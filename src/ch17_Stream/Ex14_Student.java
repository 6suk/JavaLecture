package ch17_Stream;

public class Ex14_Student {
	private String name;
	private String sex;
	private int score;

	public Ex14_Student(String name, String sex, int score) {
		super();
		this.name = name;
		this.sex = sex;
		this.score = score;
	}

	public String getName() {
		return name;
	}

	public String getSex() {
		return sex;
	}

	public int getScore() {
		return score;
	}

}
